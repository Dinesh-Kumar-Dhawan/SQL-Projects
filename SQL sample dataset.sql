use portfolio;

-- customer who done max orders and there sales

select Customer.id, concat(firstname,' ', lastname) as full_name, 
count(orders.id) as order_num, sum(totalamount) as sales
from customer
left join orders on Customer.id=orders.customerid
group by orders.customerid
order by order_num desc;


-- top 5 customers who made highest sales

select Customer.id, concat(firstname,' ', lastname) as full_name, 
sum(totalamount) as sales
from customer
left join orders on Customer.id=orders.customerid
group by orders.customerid
order by  sales desc
limit 5;

-- top sales products 

select product.id, product.productname, sum(OrderItem.quantity) as quantity, 
sum(totalamount) as total_sales
from product left join OrderItem on product.id=orderitem.productid 
left join orders on orderitem.orderid=orders.id
group by productname
order by total_sales desc;


-- top sales countries

select customer.country, sum(OrderItem.quantity) as quantity, sum(totalamount) as total_sales
from customer left join orders on customer.id=orders.customerid left join OrderItem on customer.id=orderitem.productid 
group by customer.country
order by total_sales desc;

-- sales over city, country and global

select distinct customer.country, customer.city, sum(totalamount) over(partition by customer.city) as total_sales_city,
sum(totalamount) over(partition by customer.country) as total_sales_country, sum(totalamount) over() as total_sales_global
from customer left join orders on customer.id=orders.customerid;

-- city rank over sales in country

select A.country, A.city, A.salescity, dense_rank() over(partition by country order by SalesCity desc) as city_rank
from
(select distinct customer.country, customer.city, sum(totalamount) over(partition by customer.city) as salescity,
sum(totalamount) over(partition by customer.country) as total_sales_country, sum(totalamount) over() as total_sales_global
from customer left join orders on customer.id=orders.customerid) as A;

-- sales by year

select A.year, A.yearly_sales, sum(A.yearly_sales) over() as total_sales
from
(select year(orderdate) as year, sum(totalamount) as yearly_sales
from Orders
group by year) as A
order by A.yearly_sales desc;


-- products of the year

select A.year, A.id, A.productname, A.amount, A.ranks
from
(select year(orderdate) as year, product.id, product.productname,
sum(totalamount) as amount, dense_rank() over (partition by year (orderdate) order by sum(totalamount) desc) as ranks
from product join OrderItem on product.id=orderitem.productid 
join orders on orderitem.orderid=orders.id
group by productname, year
order by year) as A
where ranks=1;




